import tkinter as tk

window = tk.Tk(className=' Artist Stat Search ') # # window is an instance of the Tk (Tkinter) class
window.geometry('1350x720')  # To set a specific size of a Tkinter window.
window.configure(bg='white') # To set background of Tkinter window to the color white.






# If application doesn't pop up, it is because we need the ".mainloop()"
window.mainloop()